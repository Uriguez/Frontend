// Función para mostrar u ocultar la letra
function mostrarLetra(idCancion, event) {
    event.preventDefault(); // Evita que la página se recargue o se desplace al inicio

    let contenedor = document.getElementById(idCancion);

    if (contenedor.classList.contains("oculto")) {
        contenedor.innerHTML = `<h3>Letra de la canción</h3><p>${letrasCanciones[idCancion]}</p>`;
        contenedor.classList.remove("oculto");
    } else {
        contenedor.classList.add("oculto");
    }
}

function toggleMenu() {
    let nav = document.getElementById("nav-menu");
    nav.classList.toggle("mostrar");
}